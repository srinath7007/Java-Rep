class Datatype
{
public static void main(String[] args)
{
byte z=12;
short x=6;
int c=123;
long v=123456;

float a=99.99f;
double b=77.777777d;

boolean s=true;
char d='$';

System.out.println(z);
System.out.println(x);
System.out.println(c);
System.out.println(v);
System.out.println(a);
System.out.println(b);
System.out.println(s);
System.out.println(d);
}
}